// This is the class declaration. 
// In Java, every application must have at least one class.
// The class name must match the filename (HelloWorld.java).
public class HelloWorld {

    // This is the main method.
    // It is the entry point of any Java application.
    // The JVM (Java Virtual Machine) calls this method to start the program.
    public static void main(String[] args) {
        
        // This line prints the text "Hello World!" to the console.
        // System is a built-in Java class that provides access to the system.
        // out is a static member of System, and it's a PrintStream object.
        // println is a method of PrintStream that prints a line of text and then moves to a new line.
        System.out.println("Hello World!");
    }
}
